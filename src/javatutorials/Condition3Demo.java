package javatutorials;


public class Condition3Demo {
 
    public static void main(String[] args) {
    	
        if (true) { // 1출력
            System.out.println(1);
        } else {
            System.out.println(2);
        }
        
        if(false){ // 2출력
            System.out.println(1);
        } else {
            System.out.println(2);
        }
 
    }
 
}
